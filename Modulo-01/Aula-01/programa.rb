# Na linha abaixo, estamos imprimindo uma mensagem na tela

puts "Hello, World!"

# A função 'puts' é usada para exibir texto no console
# A string "Hello, World!" é o conteúdo que será exibido

# Agora vou coloca o text oem maiúsculosculo

"olá, ruby".upcase

# A função 'upcase' converte todos os caracteres da string para maiúsculas
# O resultado será "OLÁ, RUBY"

# Agora vou colocar o text oem minúsculo

"OLÁ, RUBY".downcase

# A função 'downcase' converte todos os caracteres da string para minúsculas
# O resultado será "olá, ruby"

# Agora vou colocar a primeira letra em maiúsculo

"olá, ruby".capitalize

# A função 'capitalize' converte a primeira letra da string para maiúscula
# O resultado será "Olá, ruby"

# Agora operações com números
10 + 5      # Adição, resultado será 15
10 - 5      # Subtração, resultado será 5
10 * 5      # Multiplicação, resultado será 50
10 / 5      # Divisão, resultado será 2
10 % 3      # Módulo, resultado será 1 (resto da divisão)
10 ** 2     # Exponenciação, resultado será 100 (10 elevado a 2)
10.0 / 3.0  # Divisão com números de ponto flutuante, resultado será aproximadamente 3.3333
10 / 3.0    # Divisão mista, resultado será aproximadamente 3.
